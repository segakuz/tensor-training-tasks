'''Дан список словарей:
[{“наименование”: “Спички”, “цена”: 1},  {“наименование”: “Лук”, “цена”: 37},  ... ,
{“наименование”: str, “цена”: int}]
Найти ТОП-2 самых дорогих товаров и вывести в том же формате.'''


def get_tops(goods):

    return sorted(goods, key=lambda x: -x["цена"])[:2]


print(get_tops([
    {"наименование": "Спички", "цена": 1},
    {"наименование": "Лук", "цена": 37},
    {"наименование": "Товар", "цена": 666},
    {"наименование": "Ещё товар", "цена": 13},
]))
